class I18nService {
    private currentLang: Language = 'en';

    constructor() {
        // Always use English
        this.currentLang = 'en';
        localStorage.setItem('app_language', 'en');
    }

    get language() {
        return this.currentLang;
    }

    setLanguage(_lang: Language) {
        // Always force English, ignore parameter
        this.currentLang = 'en';
        localStorage.setItem('app_language', 'en');
    }

    subscribe(listener: (lang: Language) => void) {
        // Immediate notification of language
        listener(this.currentLang);
        return () => { };
    }

    t(key: keyof typeof translations.en) {
        // Always use English translations
        return translations.en[key] || key;
    }
}

export const i18n = new I18nService();

import { useState, useEffect } from 'react';

export const useLanguage = () => {
    const [language] = useState<Language>('en');

    return {
        language,
        t: (key: keyof typeof translations.en) => i18n.t(key),
        setLanguage: (_lang: Language) => { } // No-op
    };
};
